	function prefered_language($available_languages)
	{

		$http_accept_language = strtolower($_SERVER["HTTP_ACCEPT_LANGUAGE"]);

		$default_language = 'pt-br';
		$available_languages = array_flip($available_languages);

		$langs = array();
		preg_match_all('~([\w-]+)(?:[^,\d]+([\d.]+))?~', strtolower($http_accept_language), $matches, PREG_SET_ORDER);
		foreach ($matches as $match) {

			list($a, $b) = explode('-', $match[1]) + array('', '');
			$value = isset($match[2]) ? (float)$match[2] : 1.0;

			if (isset($available_languages[$match[1]])) {
				$langs[$match[1]] = $value;
				continue;
			}

			if (isset($available_languages[$a])) {
				$langs[$a] = $value - 0.1;
			}

		}
		if ($langs) {
			arsort($langs);
			return key($langs); // We don't need the whole array of choices since we have a match
		} else {
			return $default_language;
		}
	}

	function linguagens_disponiveis($app = null, $map = false)
	{

		if (!$app->Clientes_for) {
			$CI = &get_instance();
			$app = $CI->model_banco->app;
		}

		$conexoes = busca_sql("select * from conexoes where Clientes_for = '{$app->Clientes_for}' AND Linguagem_sel != ''");

		if ($map) {
			return array_map(function ($item) {
				return $item->Linguagem_sel;
			}, $conexoes);
		}
		return $conexoes;

	}

	function opcoes_linguagens($language_code = '')
	{

		$CI = &get_instance();
		$app = $CI->model_banco->app;

		$locales = [$language_code];
		if (is_array($language_code)) {
			$locales = $language_code;
		}
		if (!$language_code) {
			$locales = ResourceBundle::getLocales('');
		}

		$all_locales = [];
		$flags_dir = $app->Url_cliente . $app->Pasta_assets . 'imagens/flags/';

		foreach ($locales as $locale) {

			$locale = strtolower($locale);
			$display_language = ucfirst(locale_get_display_language($locale));
			$display_region = locale_get_display_region($locale);
			$locale_compose = locale_canonicalize($locale);
			$region = locale_get_region($locale);
			$display_language_compose = $display_region
				? "{$display_language} ({$display_region})"
				: $display_language;

			$formatter = new NumberFormatter($locale, NumberFormatter::CURRENCY);

			$object_locale = [
				'display_region' => $display_region,
				'display_language' => $display_language,
				'display_language_compose' => $display_language_compose,
				'primary_language' => locale_get_primary_language($locale),
				'region' => $region,
				'locale' => $locale,
				'locale_compose' => $locale_compose,
				'flag' => $flags_dir . strtolower($region) . '.png',
				'currency' => [
					'currency' => str_replace('-', '', $formatter->getTextAttribute(NumberFormatter::CURRENCY)),
					'currency_code' => $formatter->getTextAttribute(NumberFormatter::CURRENCY_CODE),
					'currency_symbol' => $formatter->getTextAttribute(NumberFormatter::CURRENCY_SYMBOL),
				]
			];

			$object_locale = array_to_object($object_locale);
			$all_locales[$locale] = $object_locale;

		}

		if (is_array($language_code)) {
			return $all_locales;
		} else if ($language_code) {
			return $all_locales[$language_code];
		}
ver($all_locales);
		return $all_locales;

	}
